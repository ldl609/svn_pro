
									svn常用指令
-----------------------------------------------------------------------------------------------------------------------
	指令							指令示例										指令说明	
-----------------------------------------------------------------------------------------------------------------------
 co/checkout		svn co svn://服务器地址/xxx/								checkout代码
					svn co svn://服务器地址/xxx/   /root/svnpro				checkout代码到指定目录
-----------------------------------------------------------------------------------------------------------------------
 up					svn up													更新所有文件
					svn up test.php											更新指定文件
					svn up -r 200 test.php									将版本库中的文件test.php还原到版本200
-----------------------------------------------------------------------------------------------------------------------
 add				svn add filename										添加新文件到版本库
					svn add *.php 											添加当前目录下所有php文件
					svn add . --no-ignore --force							递归添加当前目录下的所有新文件
-----------------------------------------------------------------------------------------------------------------------
 del,remove,rm		svn rm 文件名											删除文件
					svn rm svn://服务器地址/xxx/online1.0						删除分支
-----------------------------------------------------------------------------------------------------------------------
 commit/ci			svn commit -m "提交描述"									提交代码
-----------------------------------------------------------------------------------------------------------------------
 log				svn log test.php										查看指定文件的所有log
					svn log -r 100											查看指定版本号的log
-----------------------------------------------------------------------------------------------------------------------
 revert				svn revert test.php										撤销本地文件的修改（还没提交的）
					svn revert -r 目录名										撤销目录下所有本地修改
					svn revert --recursive 目录名							撤销目录下所有本地修改
-----------------------------------------------------------------------------------------------------------------------
 diff				svn diff												查看当前工作区的所有改动
					svn diff test.php 										查看当前工作区test.php文件与最新版本的差异
					svn diff -r 200:201 test.php							指定版本号比较差异
					svn diff -r 301 bin										查看当前工作区和版本301中bin目录的差异
-----------------------------------------------------------------------------------------------------------------------
 status/st			svn status												查看当前工作区的状态
-----------------------------------------------------------------------------------------------------------------------
 info				svn info												查看svn信息
-----------------------------------------------------------------------------------------------------------------------
 ls					svn ls 													查看文件列表
					svn ls -r 100											以指定-r查看,查看指定版本号的文件列表
-----------------------------------------------------------------------------------------------------------------------
 blame				svn blame filename.php									显示文件的每一行最后是谁修改的	
-----------------------------------------------------------------------------------------------------------------------
 cat				svn cat test.py -r 2									查看指定版本的文件内容,不加版本号就是查看最新版本的
-----------------------------------------------------------------------------------------------------------------------
 cleanup			svn cleanup												清理
-----------------------------------------------------------------------------------------------------------------------
 help				svn help												查看SVN帮助
					svn help commit											查看指定命令的帮助信息	
-----------------------------------------------------------------------------------------------------------------------
 merge				svn merge --reintegrate svn://服务器地址/online1.0		合并分支到主干
					svn merge svn://服务器地址/xxx/							合并主干上的最新代码到分支上,需要在分支目录下操作				 
-----------------------------------------------------------------------------------------------------------------------
 cp					svn cp -m "描述内容" 原svn地址  分支svn地址					创建分支
-----------------------------------------------------------------------------------------------------------------------
 switch				svn switch svn://服务器地址/online1.0						切换当前项目到指定分支
					svn switch --relocate 原svn地址 新svn地址					重定向仓库地址到新地址
-----------------------------------------------------------------------------------------------------------------------

 







